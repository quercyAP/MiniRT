#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ret;

	ret = ft_calloc((ft_strlen(s1) + 1), sizeof(char));
	if (!ret)
		return (NULL);
	ft_strlcpy(ret, s1, (ft_strlen(s1) + 1));
	return (ret);
}
